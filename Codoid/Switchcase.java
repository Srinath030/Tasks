public class Switchcase {
    public static void main(String[] args) {
        int n=1;
        switch(n){
            case 1:
                System.out.println("Monday");
                break; 
            case 2:
                System.out.println("Tuesday");
                break;
            case 3:
                System.out.println("Wednesday");
                break;
            case 4:
                System.out.println("Thursday");
                break;
            default:
                System.out.println("Good day");
        }
    }
}
